    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        Order order = OrderController.searchOrder(orderIdText.getText());
        if (order != null) {
            String options[] = {"Yes", "No"};
            int choice = JOptionPane.showOptionDialog(null, "Delete the Order", "Delete", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);

            switch (choice) {
                case 0:
                    boolean isDelete = OrderController.deleteOrder(orderIdText.getText());
                    if (isDelete) {
                        JOptionPane.showMessageDialog(null, "Order Deleted", "Delete Information", JOptionPane.INFORMATION_MESSAGE);
                        customerIdLabel.setText("");
                        sizeLabel.setText("");
                        qtyLabel.setText("");
                        amountLabel.setText("");
                        statusLabel.setText("");
                    }else{
                        JOptionPane.showMessageDialog(this, "Invalid Order ID..");
                    }

                    break;
                case 1:
                    dispose();
                    new DeleteOrderForm().setVisible(true);
                    break;
            }

        }
    }//GEN-LAST:event_deleteBtnActionPerformed

