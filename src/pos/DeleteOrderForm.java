    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String id = orderIdText.getText();
        Order order = OrderController.searchOrder(id);
        if (order != null) {
            deleteBtn.setEnabled(true);
            customerIdLabel.setText(order.getPNum());
            sizeLabel.setText(order.getSize());
            qtyLabel.setText(String.valueOf(order.getQty()));
            amountLabel.setText(Double.toString(order.getAmount()));
            statusLabel.setText(order.getOdStatus());
        } else {
            JOptionPane.showMessageDialog(this, "Order doesn't exist..");
            customerIdLabel.setText("");
            sizeLabel.setText("");
            qtyLabel.setText("");
            amountLabel.setText("");
            statusLabel.setText("");

        }
    }//GEN-LAST:event_searchBtnActionPerformed

